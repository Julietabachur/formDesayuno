class Producto {
    constructor (nombre , precio , descripcion, cantidad) {
       this.nombre = nombre,
       this.precio = precio,
       this.cantidad = cantidad, 
       this.descripcion = descripcion
    }
}

class Usuario {
    constructor (nombreCompleto , dni , email , celular) {
    this.nombreCompleto = nombreCompleto,
    this.dni = dni,
    this.email = email ,
    this.celular = celular
    }
}


